    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivo PDF","pdf","PDF");
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileFilter(filter);
        int result = fileChooser.showSaveDialog(null);
        if ( result == JFileChooser.APPROVE_OPTION ){
            this.ruta.setText( fileChooser.getSelectedFile().getAbsolutePath()+".pdf");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String soli = tipo.getItemAt(tipo.getSelectedIndex());
        String ruta = this.ruta.getText();
        
        if(ruta.equals("")){
            JOptionPane.showMessageDialog(null, "Seleccione una ruta");
            return;
        }
        boolean b=adminLog.generarReporteSolicitud(soli,ruta);
        if(b)
            dispose();
        

    }//GEN-LAST:event_jButton2ActionPerformed

